#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned char	*i;
	size_t			b;

	b = 0;
	i = (unsigned char *)s;
	while (b < n)
	{
		i[b] = '\0';
		b++;
	}
	return (s);
}
